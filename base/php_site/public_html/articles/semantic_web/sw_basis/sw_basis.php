                        Атомарным объектом в&nbsp;RDF является триплет: субъект&nbsp;&mdash; предикат&nbsp;&mdash;
                        объект. Считается, что любой объект, можно описать в&nbsp;терминах простых свойств и&nbsp;значений
                        этих свойств.</p>

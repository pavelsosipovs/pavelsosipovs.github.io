                    <h3>Онтологии</h3>
                    <p>Применительно к&nbsp;области Machine Learning под онтологией понимается некая структура,
                        концептуальная схема, описывающая (формализующая) значения элементов некоторой предметной
                        области (ПРО). Онтология состоит из&nbsp;набора терминов и&nbsp;правил описывающих их&nbsp;связи,
                        отношения.</p>
                    Обычно онтологии строятся из <i>экземпляров</i>, <i>понятий, атрибутов</i> и <i>отношений</i>.
                    <ul>
                        <li><i>Экземпляр</i> - элементы самого нижнего уровня. Главной целью онтологий является именно
                            классификация экземпляров, и&nbsp;хотя их&nbsp;наличие в&nbsp;онтологии не&nbsp;обязательно,
                            но&nbsp;как правило они присутствуют. <b>Пример</b>: слова, породы зверей, звёзды.
                        </li>
                        <li><i>Понятия</i> - абстрактные наборы, коллекции объектов.<br/>
                            <b>Пример</b>: Понятие &laquo;звёзды&raquo;, вложенное понятие &laquo;солнце&raquo;. Чем
                            является &laquo;солнце&raquo;, вложенным понятием или экземпляром (светилом)&nbsp;&mdash;
                            зависит от&nbsp;онтологии.<br/>
                            Понятие &laquo;светило&raquo;, экземпляр &laquo;солнце&raquo;.
                        </li>
                        <li><i>Атрибуты</i> - каждый объект может иметь необязательный набор атрибутов позволяющий
                            хранить специфичную информацию.
                            <b>Пример</b>: объект солнце имеет такие атрибуты как<br/>
                            • Тип: жёлтый карлик;<br/>
                            • Масса: 1.989 &middot; 10<sup>30</sup> кг;<br/>
                            • Радиус: 695&nbsp;990&nbsp;км.
                        </li>
                        <li>
                            <i>Отношения</i> - позволяют задать зависимости между объектами онтологии.
                        </li>
                    </ul>
                    <p>Так&nbsp;&mdash; как между различными онтологиями возможно установление точек пересечения, то&nbsp;использование
                        онтологий позволяет смотреть на&nbsp;одну ПРО с&nbsp;различных точек зрения и&nbsp;в&nbsp;зависимости
                        от&nbsp;задачи пользоваться различным уровнями детализации рассматриваемой ПРО. Понятие уровней
                        детализации онтологии является одним из&nbsp;ключевых, к&nbsp;примеру, для обозначения цвета

                            вычисления завершаются в&nbsp;определённое время). Но&nbsp;в&nbsp;связи с&nbsp;этим имеет
                            строгие ограничения, к&nbsp;примеру на&nbsp;взаимосвязи классов и&nbsp;время выполнения
                            некоторых запросов по&nbsp;таким данным могут требовать экспоненциального времени
                            выполнения.
                        </li>
                    </ul>

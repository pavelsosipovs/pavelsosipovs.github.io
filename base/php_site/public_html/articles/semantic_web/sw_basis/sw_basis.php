                        терминов и&nbsp;отношения между этими терминами в&nbsp;словарях. В&nbsp;отличии от&nbsp;RDF,
                        данный язык использует более высокий уровень абстракции, что позволяет языку наряду с&nbsp;формальной
                        семантикой использовать дополнительный терминологический словарь.</p>
                    <p>Важным преимуществом OWL является то, что его основу положена чёткая математическая модель
                        дескрипционных логик</p>

                            Гарантируется, что логический вывод на&nbsp;метаданных с&nbsp;выразительностью OWL Lite
                            выполняется за&nbsp;полиномиальное время (сложность алгоритма принадлежит классу Р).
                        </li>
                    </ul>
                    <br/>
                    Диалект основан на&nbsp;дескрипционной логике SHLF (D)<br/>
                    <ul>
                        <li>OWL DL&nbsp;&mdash; с&nbsp;одной стороны предоставляет максимальную выразительность, полноту

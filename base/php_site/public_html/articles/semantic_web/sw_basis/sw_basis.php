                        <li><acronym title="Extensible Markup Language" lang="en">XML</acronym>&nbsp;&mdash;
                            предоставляет возможности создания структурированных документов, но&nbsp;не&nbsp;предъявляет
                            к&nbsp;ним никаких семантических требований;
                        </li>
                        <li><acronym title="Extensible Markup Language" lang="en">XML</acronym> Schema&nbsp;&mdash;
                            определяет структуру <acronym title="Extensible Markup Language" lang="en">XML</acronym>
                            документов и&nbsp;дополнительно позволяет использовать конкретные типы данных;
                        </li>
                        <li>RDF&nbsp;&mdash; предоставляет возможность описывать абстрактные модели данных некоторых
                            объектов и&nbsp;отношения между ними. Использует простую семантику на&nbsp;основе <acronym
                                    title="Extensible Markup Language" lang="en">XML</acronym> синтаксиса;
                        </li>
                        <li>RDF Schema&nbsp;&mdash; позволяет описывать свойства и&nbsp;классы RDF&nbsp;&mdash;
                            ресурсов, а&nbsp;также семантику отношений между ними;

                    <h2>Программные (мобильные, пользовательские) агенты (SA)</h2>
                    <p>В&nbsp;рассматриваемой ПРО SA&nbsp;считается программой, действующей от&nbsp;имени пользователя,
                        самостоятельно выполняющей сбор информации на&nbsp;протяжении некоторого, возможно длительного
                        времени. Также важной их&nbsp;особенностью является возможность взаимодействовать с&nbsp;другими
                        агентами и&nbsp;сервисами для достижения поставленной цели.<br/>
                        В&nbsp;отличии от&nbsp;ботов поисковых машин, которые просто сканируют диапазоны WEB страниц,
                        агенты перемещаются от&nbsp;сервера к&nbsp;серверу,
                        <nobr>т.&nbsp;е.</nobr>
                        на&nbsp;отправном сервере он&nbsp;уничтожается, а&nbsp;на&nbsp;принимающем создаётся с&nbsp;полным
                        набором собранной ранее информации. Такая модель позволяет агенту использовать доступные
                        серверу, источники данных, которые не&nbsp;доступны посредством WEB интерфейса.<br/>
                        Понятно, что на&nbsp;сервере должна быть установлена платформа, позволяющая принять агента и&nbsp;обслужить
                        его запросы. Также важно уделить внимание безопасности и&nbsp;целостности агентов. Для этого
                        применяется подход выделенных пространств, когда агент работает в&nbsp;некотором безопасном
                        окружении с&nbsp;ограниченными правами и&nbsp;возможностями воздействия на&nbsp;систему.<br/>
                        Агенты по&nbsp;своей реализации делятся на&nbsp;обычные и&nbsp;обучающиеся.<br/>
                        Если первые предназначены для выполнения чётко поставленных задач, то&nbsp;в&nbsp;основу вторых
                        заложена гибкость, обычно они создаются на&nbsp;основе нейронных сетей. Использование нейронных
                        сетей позволяет агенту постоянно подстраиваться под требования пользователя, а&nbsp;также более
                        эффективно взаимодействовать с&nbsp;Internet.
                    </p>

